public class Ex09_03 {

	public static void main(String[] args) {
		// [비교연산자]  >    <   >=    <=    ==(같다)    !=(다르다)
		// true/false 논리형으로 결과값 나온다.
		
	System.out.println(3>5);   //false
	System.out.println(3<5);   //true
	System.out.println(3>=5);   //false
	System.out.println(3<=5);   //true
	
	//주의
	
	
	System.out.println(3==5);   //false
	System.out.println(3!=5);    //true
	}
}
